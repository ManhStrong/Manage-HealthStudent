    private void tblQuanlyMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblQuanlyMouseClicked
        // TODO add your handling code here:
        selectedIndex = tblQuanly.getSelectedRow();
        if (selectedIndex != -1) {
            ArrayList<DonXinNghiGV> dxn = new ArrayList<DonXinNghiGV>(DonXinNghiGV.dxngvs);
            d = dxn.get(selectedIndex);
            txtMa.setText(d.getMaDon());
            txtMaGV.setText(d.getMaGV());
            txtLyDo.setText(d.getLyDo());
            txtNgayBatDau.setText(d.getNgayBatDauNghi());
            txtNgayKetThuc.setText(d.getNgayKetThucNghi());
            txtAnh.setText(d.getAnh());
            txtNgayNop.setText(d.getNgayNop());
            txtTTSK.setText(d.gettTSK());
            if (d.isTtDon()) {
                btlDuyet.setSelected(true);
            } else {
                btlKhongDuyet.setSelected(true);
            }
            String link = d.getAnh().substring(d.getAnh().lastIndexOf("\\") + 1);
            try {
                BufferedImage img = null;
                img = ImageIO.read(new File("./src/File/Image/DonXinNghi/" + link));
                Image anh = img.getScaledInstance(showAnh.getWidth(), showAnh.getHeight(), Image.SCALE_SMOOTH);
                showAnh.setIcon(new ImageIcon(anh));
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.toString());
            }
        }
    }//GEN-LAST:event_tblQuanlyMouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        selectedIndex = tblQuanly.getSelectedRow();
        if (selectedIndex != -1) {
            DonXinNghiGV temp = new DonXinNghiGV();
            ArrayList<DonXinNghiGV> tt = new ArrayList<DonXinNghiGV>(DonXinNghiGV.dxngvs);
            temp = tt.get(selectedIndex);
            if (btlDuyet.isSelected()) {
                temp.setTtDon(true);
            } else {
                temp.setTtDon(false);
            }
            tt.set(selectedIndex, temp);
            DonXinNghiGV.dxngvs = new TreeSet<DonXinNghiGV>(tt);
            fillTable();
            DonXinNghiGV.writeData();
        }
    }//GEN-LAST:event_jButton1ActionPerformed
