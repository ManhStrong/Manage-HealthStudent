    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        selectedIndex = tblQuanly.getSelectedRow();
        if (selectedIndex != -1) {
            int select = JOptionPane.showConfirmDialog(this, "Xác nhận xóa");
            if (select == 0) {
                ArrayList<DonXinNghiGV> ds = new ArrayList<DonXinNghiGV>(DonXinNghiGV.dxngvs);
                DonXinNghiGV.dxngvs.remove(ds.get(selectedIndex));
                fillTable();
                JOptionPane.showMessageDialog(this, "Xóa thành công");
                Clear();
                DonXinNghiGV.writeData();
            } else {
                JOptionPane.showMessageDialog(this, "Chưa chọn dữ liệu");
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed

