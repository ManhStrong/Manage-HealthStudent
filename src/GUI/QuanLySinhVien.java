    public void fillDataIntoCbo()
    {
        ArrayList<String> ds = new ArrayList<String>();
        for (TaiKhoan tk : TaiKhoan.tks) {
            boolean check = true;
            for (SinhVien sv : SinhVien.svs) {
                if(tk.getUsername().equals(sv.getTenTaiKhoan()))
                {
                    check = false;
                }
            }
            if(check && tk.getRole()==0)
            {
                ds.add(tk.getUsername());
            }
        }
        for (String s : ds) {
            cbotk.addItem(s);
        }
    }
