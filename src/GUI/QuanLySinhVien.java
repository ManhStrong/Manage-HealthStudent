    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        if (Check()) {
            String temp = "";
            if (radiobb.isSelected()) {
                temp = "Bị Bệnh";
            }
            if (radiobt.isSelected()) {
                temp = "Bình thường";
            }
            sv = new SinhVien(cboKhoa.getSelectedItem().toString(), txtLop.getText(),txtMa.getText(),txtTen.getText(), txtDiaChi.getText(), txtSDT.getText()
            , txtNgaySinh.getText(), temp, txtAnh.getText(), cbotk.getSelectedItem().toString());
            SinhVien.svs.add(sv);
            cbotk.remove(cbotk.getSelectedIndex());
            fillTable();
            SinhVien.writeData();
            fillDataIntoCbo();
            

        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        selectedIndex = tblStudent.getSelectedRow();
        if (selectedIndex != -1) {
            ArrayList<SinhVien> ds = new ArrayList<SinhVien>(SinhVien.svs);
            if (Check()) {
                String temp = "";
                if (radiobb.isSelected()) {
                    temp = "Bị Bệnh";
                }
                if (radiobt.isSelected()) {
                    temp = "Bình thường";
                }
                sv = new SinhVien(cboKhoa.getSelectedItem().toString(), txtLop.getText(),txtMa.getText(),txtTen.getText(), txtDiaChi.getText(), txtSDT.getText()
            , txtNgaySinh.getText(), temp, txtAnh.getText(), cbotk.getSelectedItem().toString());
                ds.set(selectedIndex, sv);
                SinhVien.svs = new TreeSet<SinhVien>(ds);
                fillTable();
                JOptionPane.showMessageDialog(this, "Sửa thành công");
                SinhVien.writeData();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Chưa chọn dòng sửa");
        }
    }//GEN-LAST:event_btnSuaActionPerformed

