    private void tblStudentMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblStudentMouseClicked
        selectedIndex = tblStudent.getSelectedRow();
        if (selectedIndex != -1) {
            ArrayList<SinhVien> ds = new ArrayList<SinhVien>(SinhVien.svs);
            sv = ds.get(selectedIndex);
            txtMa.setText(sv.getMa());
            txtTen.setText(sv.getHoTen());
            txtSDT.setText(sv.getSdt());
            txtAnh.setText(sv.getAnh());
            try {
                BufferedImage img = null;
                img = ImageIO.read(new File(sv.getAnh()));
                Image img1 = img.getScaledInstance(anh.getWidth(), anh.getHeight(), Image.SCALE_SMOOTH);
                anh.setIcon(new ImageIcon(img1));
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.toString());
            }
            txtDiaChi.setText(sv.getDiaChi());
            txtLop.setText(sv.getLop());
            if (sv.getTtsk().compareTo("Bình thường") == 0) {
                radiobt.setSelected(true);
            }
            if (sv.getTtsk().compareTo("Bị Bệnh") == 0) {
                radiobb.setSelected(true);
            }

            txtNgaySinh.setText(sv.getNgaySinh());
            cboKhoa.setSelectedItem(sv.getKhoa());
            cbotk.setSelectedItem(sv.getTenTaiKhoan());
        }
        
    }//GEN-LAST:event_tblStudentMouseClicked

    public  void validateJTextField(JTextField field,StringBuilder sb,String error){
        if(field.getText().equals("")){
            sb.append(error).append("\n");
            field.setBackground(Color.red);
            field.requestFocus();
        }
        else{
            field.setBackground(Color.white);
        }   
    }
    private Boolean Check() {
        StringBuilder sb = new StringBuilder();
        String error= "";
        validateJTextField(txtMa, sb, "Mã không được rỗng");
        validateJTextField(txtTen, sb, "Tên không được rỗng");
        validateJTextField(txtDiaChi, sb, "Địa chỉ không được rỗng");
        validateJTextField(txtLop, sb, "Lớp không được trống");
        validateJTextField(txtNgaySinh, sb, "Ngày sinh không được rỗng");
        validateJTextField(txtSDT, sb, "Số điện thoại không được để trống");
        if(radiobb.isSelected() == false && radiobt.isSelected()== false)
        {
             error = " Chưa chọn tình trạng bệnh";
        }
        if(sb.length() >0)
        {
            JOptionPane.showMessageDialog(this, sb.toString()+ error );
            return false;
        }
        return true;
    }

